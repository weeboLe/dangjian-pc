          <p class="title">五、 工作步骤</p>
          <p class="title"> (一)村级党组织领导班子换届选举</p>
          <p class="title">村级党组织领导班子换届选举要按照《中国共产党农村基层组织工作条例》《中国共产党基层组织选举工作暂行条例》等有关规定要求，实行“两推一选”，即在镇(街道)党(工)委的领导和指导下，根据党员和群众推荐、镇(街道)党(工)委推荐的情况，由村党组织研究提出候选人预备人选，经镇(街道)党(工)委批准后进行选举。
          </p>
          <p class="title">1.准备工作阶段(3月5日前)。换届前，村党组织召开委员会议，讨论通过“关于召开党员大会的决定”，并向镇(街道)党(工)委呈报关于召开党员大会的请示。镇(街道)党(工)委批复后，村党组织及时召开委员和党小组长会议，传达镇(街道)党(工)委批复要求，研究召开党员大会相关事宜。
          </p>
          <p class="title">2.民主测评和党员群众推荐、镇(街道)党(工)委推荐防段(3月10日前)。</p>
          <p class="title">①民主测评，镇(街道)党(工)委组织召开村党员和村氏代表会议，对现任村党组织领导班子及成员进行民主测评。测评结果作为确定候选人预备人选的重要参考依据。
          </p>
          <p class="title">②党员群众推荐。 民主测评后，由村党组织召开党员和村氏代表会议，推荐下届村党组织委员候选人初步人选。参加推荐的党员和村民代表超过应到会人数一半以上，推荐有效。村党组织在得票数超过推荐票一半以，上的党员中，按照不低于委员职数20%的差额比例，提出村党组织委员候选人初步人选。推荐的候选人人数不足法定推荐人数时，缺额人选在未入围得票较多的被推荐人中另行推荐产生。推荐结果报镇(街道)党(工)委。
          </p>
          <p class="title">③镇(街道)党 (工)委推荐。镇(街道)党(工)委召开会议，结合民主测评情况和研判结果，按照不低于委员职数20%的差额比例，推荐下届村党组织委员候选人初步人选，并将名单通报村党组织。
          </p>
          <p class="title">3.确定考察人选和候选人预备人选阶段(3月18日前)。</p>
          <p class="title">①确定考察人选。村党组织召开委员会议，结合“两推”情况，按照不低于委员职数20%的差额比例，确定上报预备人选名单。镇(街道)党(工)委书面征求纪检监察、信访、卫计、公安、环保、国土、法院等部门意见，审核预备人选资格条件。由镇(街道)党(工)委对审核通过的预备人选进行组织考察。
          </p>
          <p class="title">②确定候选人预备人选。村党组织召开委员会议，根据镇(街道)党(工)委考察情况，研究确定下届村党组织委员会委员预备人选名单，并在全村范围内进行公示，公示时间不少于5日。公示结束无异议，预备人选报镇(街道)党(工)委批复。
          </p>
          <p class="title">4. 党内选举(3月20日前)。村党组织按照有关规定和程序先召开党员大会，差额选举产生新届村党组织委员会委员，然后召开村党组织委员会议，选举产生书记、副书记。党员大会选举时，有选举权的到会党员数超过应到会党员数的五分之四，会议有效;候选人获得的赞成果超过实到会有选举权的人数的一半，始得当选。选举结束后，选举结果报镇(街道)党(工)委批复。镇(街道)党(工)委应于10个工作日内予以书面批复。
          </p>
          <p class="title">对村风正、 村情稳的村，也可实行“双推双审双选”。“双推”:党员和群众推荐、镇(街道)党(工)委推荐;“双审”:村级党组织研究审议提出候选人预备人选，镇(街道)党(工)委研究审定提出正式候选人;“双选”:村党员大会选举产生村级党组织委员，再由党员大会从当选委员中选举产生书记、副书记。
          </p>
          <p class="title">党员因下列情况不能参加选举的，经报镇(街道)党(工)委同意，并经村党员大会通过，可以不计算在应到会人数之内:(1)患有精神病或其他疾病导致不能表达本人意志的; (2)虽未受到留党察看以上党纪处分，但正在拘留或服刑的; (3)年老体弱卧床不起或长期生活不能自理的; (4)长期居住外地或外出务工超过半年以上，经村党组织通知，本人确认不能返回参加换届选举的。
