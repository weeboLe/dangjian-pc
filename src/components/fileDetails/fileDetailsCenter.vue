            束3个月内完成。</p>
          <p class="title">各镇(街道)要在村级组织换届选举工作结束后形成工作总结，与村(社区)党组织、村(居)委会、村(社区)监委会组分别上报县委组织部、县民政局。</p>
          <p class="title">四、人选条件</p>
          <p class="title">各镇(街道)要严格按照有关法律法规和中省市县有关政策要求，认真开展调查摸底，结合实际把握好新一届村党组织和村委会班子成员候选人的选拔推荐条件。村党组织领导班子和村安会成员应具备以下条件: (一)政治过硬，对党忠诚，坚决维护习近平同志核心地位和党中央权威; (二)锐意创新，能谋善战，有较强组织领导能力; (三)心系群众，办事公道，有较强的奉献精神; (四)廉洁自律，作风正派，有较好群众基础和口碑;(五)身体健康，有初中以上文化程度，年龄一般在55周岁以下。 能正常履行工作职责、实绩突出、群众威信高，受到县委、县政府以上表彰、特别优秀的，年龄可适当放宽，但原则不得超过60周岁。符合放宽条件的，由镇(街道)党(工)委报县换届选举工作领导小组审批，要控制在一定比例之内。
