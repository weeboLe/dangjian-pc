          <h3 class="bigFont">山阳县村级党组织领导班子和 第十次村民委员会换届选举工作安排意见</h3>
          <p class="title">按照《中国共产党农村基层组织工作条例》《中华人民共和国村民委员会组织法》《中共中央办公厅、  国务院办公厅关于建立健全村务监督委员会的指导意见》规定和省市《村级党组织领立选手和第十次村民委员会换届选举工作安排意见》，我县从2018年12月开始进行全县村级党组
            织领导班子和第十次村民委员会换届选举工作。为切实做好换届选举相关工作，保障全县村“两委”班子换届选举公正有序进行，现提出如下安排意见。</p>
